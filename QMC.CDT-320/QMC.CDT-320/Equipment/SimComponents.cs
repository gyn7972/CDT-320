using QMC.Common.IO;
using QMC.Common.Motion;

namespace QMC.CDT320
{
    // ??????????????????????????????????????????????????????????????????????????
    //  SimDigitalOutput ? 시뮬레이션 전용 디지털 출력 구현체
    // ??????????????????????????????????????????????????????????????????????????

    /// <summary>
    /// 시뮬레이션 전용 <see cref="BaseDigitalOutput"/> 구현체.<br/>
    /// 실제 I/O 보드 없이 논리 상태만 관리한다.
    /// </summary>
    public class SimDigitalOutput : BaseDigitalOutput
    {
        /// <summary><see cref="SimDigitalOutput"/>을 초기화한다.</summary>
        /// <param name="name">DO 포인트 이름</param>
        public SimDigitalOutput(string name) : base(name) { }
    }

    // ??????????????????????????????????????????????????????????????????????????
    //  SimDigitalInput ? 시뮬레이션 전용 디지털 입력 구현체
    // ??????????????????????????????????????????????????????????????????????????

    /// <summary>
    /// 시뮬레이션 전용 <see cref="BaseDigitalInput"/> 구현체.<br/>
    /// 실제 센서 없이 <see cref="BaseDigitalInput.SimulateInput"/>으로만 상태를 주입받는다.
    /// </summary>
    public class SimDigitalInput : BaseDigitalInput
    {
        /// <summary><see cref="SimDigitalInput"/>을 초기화한다.</summary>
        /// <param name="name">DI 포인트 이름</param>
        public SimDigitalInput(string name) : base(name) { }
    }

    // ??????????????????????????????????????????????????????????????????????????
    //  SimAxis ? 시뮬레이션 전용 모션 축 구현체
    // ??????????????????????????????????????????????????????????????????????????

    /// <summary>
    /// 시뮬레이션 전용 <see cref="BaseAxis"/> 구현체.<br/>
    /// 실제 모션 보드 없이 <see cref="BaseAxis"/>의 내장 시뮬레이션 엔진만 사용한다.
    /// </summary>
    public class SimAxis : BaseAxis
    {
        /// <summary><see cref="SimAxis"/>를 초기화한다.</summary>
        /// <param name="name">축 이름</param>
        public SimAxis(string name) : base(name) { }
    }

    // ??????????????????????????????????????????????????????????????????????????
    //  SimCylinder ? 시뮬레이션 전용 실린더 구현체
    // ??????????????????????????????????????????????????????????????????????????

    /// <summary>
    /// 시뮬레이션 전용 <see cref="BaseCylinder"/> 구현체.<br/>
    /// 내부 DI/DO를 <see cref="SimDigitalInput"/> / <see cref="SimDigitalOutput"/>으로 구성한다.
    /// </summary>
    public class SimCylinder : BaseCylinder
    {
        /// <summary><see cref="SimCylinder"/>를 초기화한다.</summary>
        /// <param name="name">실린더 이름</param>
        public SimCylinder(string name) : base(name) { }

        /// <inheritdoc/>
        protected override BaseDigitalOutput CreateDigitalOutput(string name)
            => new SimDigitalOutput(name);

        /// <inheritdoc/>
        protected override BaseDigitalInput CreateDigitalInput(string name)
            => new SimDigitalInput(name);
    }
}
